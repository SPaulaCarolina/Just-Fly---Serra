class Destino {
        constructor(nombre,region,pais) {
                this.nombre= nombre;
                this.region= region;
                this.pais = pais;
        }
}

class Vuelo {
        //CONSTRUCTOR DE CLASE PRODUCTO
        constructor(origen,destino,fecha,hora,id,nombre,precio) {
                this.origen = origen;
                this.destino = destino;
                this.fecha = fecha;
                this.hora = hora;
                this.id = id;
                this.nombre = nombre;
                this.precio = precio;
        }
}
